pruned_input = os.path.join(here, "ap-dump-pruned.json")
output_py = os.path.join(here, "generated_names.py")

def main():
    import argparse
    parser = argparse.ArgumentParser()
    parser.add_argument("input", metavar="path/to/script-output/ap-dump.json", help=
        "See FactorioInformationExtractor for how to generate this.")
    args = parser.parse_args()

    process(args.input)

header = """\
#################################################
# This file is generated by ./import-ap-dump.py #
#################################################

# This file provides spell checking in the Python type system,
# for example catching the misspelling "metalurgic", which is common for me.
# Because item, plant, technology, etc. names are all mixed together,
# this doesn't function as proper type checking, just spell checking;
# You're on you're own for remembering the difference between accumulator and electric_energy_accumulators.

"""

def process(input_path):
    # Import the json data and gzip it.
    with open(input_path, "rb") as f:
        the_data = json.load(f)
    with open(informative_input, "w") as f:
        json_dump(the_data, f)
    the_data = prune_data(the_data)
    with open(pruned_input, "w") as f:
        json_dump(the_data, f)

    # Generate constants for some things we might want to refer to.
    already_generated = {}
    lines = []
    for prototype_type, prototype_decls in the_data.items():
        if prototype_type in (
            "noise-expression",
            "noise-function",
            "projectile",
        ): continue
        for name in prototype_decls.keys():
            python_name = to_snake(name)
            line = "{} = {}".format(python_name, json.dumps(name))
            if python_name not in already_generated:
                lines.append(line + "\n")
                already_generated[python_name] = line
            else:
                assert already_generated[python_name] == line, "identifier collision: " + line
    lines.sort()
    with open(output_py + ".tmp", "w") as f:
        f.write(header)
        f.writelines(lines)
    os.rename(output_py + ".tmp", output_py)

def to_snake(name):
    if name[0].isdigit():
        name = "_" + name
    return name.lower().replace("-", "_")

