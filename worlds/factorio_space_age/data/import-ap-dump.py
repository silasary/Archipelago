def prune_data(data):
    def recurse(data, schema):
        if schema == object:
            return data
        if schema == str:
            assert type(data) == str, "expected str: " + repr(data)
            return data
        if schema == bool:
            assert type(data) == bool, "expected bool: " + repr(data)
            return data
        if schema == int:
            assert type(data) in (int, float), "expected number: " + repr(data)
            # Recipe product amounts are sometimes floats in the prototype data (example "speed-module-recycling").
            # They get truncated to int16 by the runtime phase.
            return int(data)
        if schema == float:
            assert type(data) in (int, float), "expected number: " + repr(data)
            return data
        if type(schema) == dict and schema.keys() == {str}:
            [v_schema] = schema.values()
            return {k: recurse(v, v_schema) for k, v in data.items()}
        if type(schema) == dict and all(type(k) == str for k in schema.keys()):
            return {k: recurse(data[k], v_schema) for k, v_schema in schema.items() if k in data}
        if type(schema) == list and len(schema) == 1:
            [v_schema] = schema
            if type(data) == list:
                return [recurse(v, v_schema) for v in data]
            else:
                return recurse(data, v_schema) # Fallback to guessing the list is instead a single item.
        if type(schema) == tuple:
            assert type(data) == list and len(schema) == len(data), "expected tuple of length {}: {}".format(len(schema), repr(data))
            return [recurse(v, v_schema) for v, v_schema in zip(data, schema)]
        assert False, "unrecognized schema: " + repr(schema)
    return recurse(data, keep_props)

