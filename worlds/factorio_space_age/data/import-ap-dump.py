def process(input_path):
    # Import the json data and gzip it.
    with open(input_path, "rb") as f:
        the_data = json.load(f)
    with open(informative_input, "w") as f:
        json_dump(the_data, f)
    the_data = prune_data(the_data)
    with open(pruned_input, "w") as f:
        json_dump(the_data, f)

    generate_names(the_data)
    generate_ids(the_data)

def generate_names(the_data):
    # Generate spellchecking names.
    already_generated = {}
    builtin_name_lines = []
    for prototype_type, prototype_decls in the_data.items():
        if prototype_type == "projectile": continue # Never interesting.
        for name in prototype_decls.keys():
            python_name = to_snake(name)
            line = "{} = {}".format(python_name, json.dumps(name))
            if python_name not in already_generated:
                builtin_name_lines.append(line + "\n")
                already_generated[python_name] = line
            else:
                assert already_generated[python_name] == line, "identifier collision: " + line
    builtin_name_lines.sort()

    artificial_name_lines = []
    for name in ap_item_names + trap_names:
        python_name = to_snake(name.lower().replace(" ", "_"))
        line = "{} = {}".format(python_name, json.dumps(name))
        assert python_name not in already_generated, "identifier collision: " + line
        already_generated[python_name] = line
        artificial_name_lines.append(line + "\n")
    for name, technology_names in progressive_technology_stacks.items():
        for technology_name in technology_names:
            assert technology_name in the_data["technology"].keys(), "technology not found: " + technology_name
        if name in the_data["technology"].keys():
            # Some of the progressive stacks are just the name of the infinite tech.
            continue
        python_name = to_snake(name)
        line = "{} = {}".format(python_name, json.dumps(name))
        assert python_name not in already_generated, "identifier collision: " + line
        already_generated[python_name] = line
        artificial_name_lines.append(line + "\n")

    with open(output_names_py, "w") as f:
        f.write(header)
        f.write(names_header)
        f.writelines(builtin_name_lines)
        f.write(artificial_names_header)
        f.writelines(artificial_name_lines)

def generate_ids(the_data):
    # Generate AP id numbers.
    factorio_base_id = 2 ** 17
    id_cursor = factorio_base_id
    def next_id():
        nonlocal id_cursor
        id_cursor += 1
        return id_cursor
    # Technology locations and items
    ap_location_name_to_id = {}
    ap_item_name_to_id = {}
    for technology_name, technology_data in sorted(the_data["technology"].items()):
        assert not "_" in technology_name, "that's the delimiter we use for our special suffixes: " + technology_name
        location_name = technology_name + "_location"
        ap_location_name_to_id[location_name] = next_id()
        if (
            technology_name not in unrandomized_technologies and
            technology_data.get("max_level", 1) != "infinite" and
            "unit" in technology_data # Researched with science packs, not a trigger.
        ):
            second_location_name = technology_name + "_other_location"
            ap_location_name_to_id[second_location_name] = next_id()
        ap_item_name_to_id[technology_name] = next_id()
    # Progressive pseudo items
    for progressive_technology_name in sorted(progressive_technology_stacks):
        if progressive_technology_name in ap_item_name_to_id:
            # Some of the progressive stacks are just the name of the infinite tech.
            continue
        ap_item_name_to_id[progressive_technology_name] = next_id()
    # New items
    for name in ap_item_names:
        ap_item_name_to_id[name] = next_id()
    # Traps
    for trap_name in trap_names:
        ap_item_name_to_id[trap_name] = next_id()

    with open(output_ids_py, "w") as f:
        f.write(header)
        f.write(ids_header)
        f.write("ap_location_name_to_id = {}\n".format(json.dumps(ap_location_name_to_id, indent=4)))
        f.write("\n")
        f.write("ap_item_name_to_id = {}\n".format(json.dumps(ap_item_name_to_id, indent=4)))

def to_snake(name):
    if name[0].isdigit():
        name = "_" + name
    return name.lower().replace("-", "_")


